create table Usuario (
idUser int primary key auto_increment,
nome varchar(30),
email varchar(50) unique,
senha varchar(30)
) auto_increment = 10000;

create table Conquista (
idConquista int primary key auto_increment,
nome varchar(80)
) auto_increment = 123;

create table UserXConquista (
fkUser int,
fkConquista int,
primary key (fkUser, fkConquista)
);

create table Build (
idBuild int auto_increment,
fkUser int,
primary key (idBuild, fkUser),
nome varchar(45)
)auto_increment = 50000;


create table Atributos (
fkBuild int,
fkUser int,
idAtr int auto_increment,
primary key (idAtr, fkBuild, fkUser),
Nivel int,
Vigor int,
Conhecimento int,
Fortitude int,
Vitalidade int,
Forca int,
Destreza int,
Inteligencia int,
Fe int,
Sorte int
);

select * from Atributos;

create table Armas (
idArma int primary key auto_increment,
nome varchar(64),
tipo varchar(30),
peso decimal(3,1),
DanoFisico int,
DanoMagico int,
DanoFlamejante int,
DanoEletrico int,
DanoSombrio int,
Critico int,
BuffMagia int
);


create table Feiticos (
idFeitico int primary key auto_increment,
nome varchar(45),
tipo varchar(45),
descricao varchar(200),
dano decimal(4,3),
cura decimal(4,3),
buff decimal(4,3)
);

create table Armaduras (
idArmaduras int primary key auto_increment,
nome varchar(45),
tipo varchar(45),
peso decimal(3,1),
DefesaFisica decimal(3,1),
DefesaMagica decimal(3,1),
DefesaFlamejante decimal(3,1),
DefesaEletrica decimal(3,1),
DefesaSombria decimal(3,1)
);

create table Anel (
idAnel int primary key auto_increment,
nome varchar(50),
peso decimal(3,1),
efeito text
);

create table ArmaXBuild (
fkArmas int,
fkBuild int,
fkUser int,
primary key (fkArmas, fkBuild, fkUser),
Slot int
);

create table FeiticoXBuild (
fkFeitico int,
fkBuild int,
fkUser int,
primary key (fkFeitico, fkBuild, fkUser),
Slot int
);

create table ArmaduraXBuild (
fkArmadura int,
fkBuild int,
fkUser int,
primary key (fkArmadura, fkBuild, fkUser),
Slot int
);

create table AnelXBuild (
fkAnel int,
fkBuild int,
fkUser int,
primary key (fkAnel, fkBuild, fkUser),
Slot int
);

